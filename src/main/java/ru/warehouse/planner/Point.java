
package ru.warehouse.planner;

public class Point{
    public double y;
    public double x;

    public void setY(double scan) {
        scan = y;
    }

    public void setX(double scan) {
        scan = x;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}

